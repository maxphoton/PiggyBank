"""–†–æ—É—Ç–µ—Ä –¥–ª—è —Ä–∞—Å—Å—ã–ª–∫–∏ —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º FSM —Å–æ—Å—Ç–æ—è–Ω–∏–π."""

import logging

from aiogram import Bot, Router, types
from aiogram.filters import Command, StateFilter
